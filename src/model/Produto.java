package model;

/**
 *
 * @author ueslei
 */
public class Produto {
    private int id;
    private String nome;
    private double valor;
    private String marca;
    private int estoque;

    public Produto(){
    
    }
    
    public Produto(int id, String nome, double valor, String marca, int estoque) {
        this.id = id;
        this.nome = nome;
        this.valor = valor;
        this.marca = marca;
        this.estoque = estoque;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getEstoque() {
        return estoque;
    }

    public void setEstoque(int estoque) {
        this.estoque = estoque;
    }
    
}
